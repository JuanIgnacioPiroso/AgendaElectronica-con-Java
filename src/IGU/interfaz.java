    private void BtnAdelanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAdelanteActionPerformed

        if (registrosActuales < 9) {

            registrosActuales++;
            txtNombre.setText(nombres[registrosActuales]);
            txtApellido.setText(apellidos[registrosActuales]);
            txtNacimiento.setText(nacimiento[registrosActuales]);
            txtDireccion.setText(direccion[registrosActuales]);
            txtTelefono.setText(telefono[registrosActuales]);
            txtDni.setText(dni[registrosActuales]);

        }

        txtIndice.setText(String.valueOf(registrosActuales)); //Mostrar el indice actual en el textfield


    }//GEN-LAST:event_BtnAdelanteActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        //Guardo en las variables los contenidos de los text fields en su indice actual
        nombres[registrosActuales] = txtNombre.getText();
        apellidos[registrosActuales] = txtApellido.getText();
        dni[registrosActuales] = txtDni.getText();
        nacimiento[registrosActuales] = txtNacimiento.getText();
        telefono[registrosActuales] = txtTelefono.getText();
        direccion[registrosActuales] = txtDireccion.getText();

    }//GEN-LAST:event_btnGrabarActionPerformed

    private void btnAtrasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtrasActionPerformed

        if (registrosActuales > 0) {

            registrosActuales--;
            txtNombre.setText(nombres[registrosActuales]);
            txtApellido.setText(apellidos[registrosActuales]);
            txtNacimiento.setText(nacimiento[registrosActuales]);
            txtDireccion.setText(direccion[registrosActuales]);
            txtTelefono.setText(telefono[registrosActuales]);
            txtDni.setText(dni[registrosActuales]);

        }
        txtIndice.setText(String.valueOf(registrosActuales));//Mostrar el indice actual en el textfield
    }//GEN-LAST:event_btnAtrasActionPerformed

